function generateTiAndRssHTML(language = 'es') {
  const container = document.getElementById('ti-and-rss');
  container.innerHTML = '';

  const data = tiAndRssData[language];

  data.forEach(sectionData => {
    const sectionDiv = document.createElement('div');
    sectionDiv.classList.add('portfolio-container');

    const sectionTitle = document.createElement('h2');
    sectionTitle.textContent = sectionData.title;
    sectionDiv.appendChild(sectionTitle);

    const sectionDescription = document.createElement('p');
    sectionDescription.textContent = sectionData.description;
    sectionDiv.appendChild(sectionDescription);

    const cardContainer = document.createElement('div');
    cardContainer.classList.add('portfolioCard-container');

    sectionData.sections.forEach(brandData => {
      if (sectionData.title === "TI" || sectionData.title === "IT") {
        brandData.links.forEach(linkData => {
          const portfolioCard = document.createElement('div');
          portfolioCard.classList.add('portfolioCard');

          const img = document.createElement('img');
          img.src = linkData.image;
          img.alt = linkData.title;
          portfolioCard.appendChild(img);

          const cardTitle = document.createElement('h3');
          cardTitle.textContent = linkData.title;
          portfolioCard.appendChild(cardTitle);

          portfolioCard.addEventListener('click', () => {
            window.open(linkData.link, '_blank');
          });

          cardContainer.appendChild(portfolioCard);
        });
      } else {
        const portfolioCard = document.createElement('div');
  portfolioCard.classList.add('portfolioCardRSS');

  const img = document.createElement('img');
  img.src = brandData.logo;
  img.alt = brandData.brand;
  img.classList.add('back')
  portfolioCard.appendChild(img);

  const linksContainer = document.createElement('div');
  linksContainer.classList.add('links-container');

  brandData.links.forEach(linkData => {
    const iconAnchor = document.createElement('a');
    iconAnchor.href = linkData.link;
    iconAnchor.target = '_blank';

    const iconImg = document.createElement('img');
    iconImg.src = linkData.icon;
    iconImg.alt = linkData.link.split('/')[2];
    
    iconAnchor.appendChild(iconImg);
    linksContainer.appendChild(iconAnchor);
  });

  portfolioCard.appendChild(linksContainer);
  cardContainer.appendChild(portfolioCard);

      }
    });

    sectionDiv.appendChild(cardContainer);
    container.appendChild(sectionDiv);
  });
}

document.addEventListener('DOMContentLoaded', () => {
  generateTiAndRssHTML();

  document.getElementById('navbar-container').addEventListener('click', (event) => {
    if (event.target.id === 'language-toggle') {
      const newLanguage = event.target.textContent === 'Español' ? 'es' : 'en';
      generateTiAndRssHTML(newLanguage);
    }
  });
});


